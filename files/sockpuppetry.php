<h2>Introduction</h2>
<p>Because we allow new (anonymous) user self-signup, accounts do not need to be confirmed. Thus, a person could register a fake second account, and he or she will not be required to certify that the two accounts are unique.</p>
<p>Due to this, it is quite common for a person that is losing an argument to sign up for a second account just to create an illusion of increased support for that person's side.</p>
<p>This document covers:</p>
<ul>
<li>What is a sockpuppet?</li>
<li>What are some illegal and legal uses of a sockpuppet?</li>
<li>How do I catch a sockpuppet?</li>
<li>How do I report a possible sockpuppet?</li>
<li>I've been accused of having a sockpuppet, and I don't!</li>
<li>I've been accused, and I have a sockpuppet. What now?</li>
</ul>
<h2>What is a sockpuppet?</h2>
<p>Simply put, a sockpuppet is a non-primary account created by a person who already has an account. The person might use an alternative username, a different social media link, and a different profile to make the sockpuppet look like a different person. There are many uses for a sockpuppet, some legal, some illegal. These uses are discussed in the next section.</p>
<h2>Uses of sockpuppets</h2>
<p>The following uses are <strong>illegal</strong> and probably will get you banned:</p>
<ul>
<li>Gaming the results of a poll by casting multiple votes for the same or different options</li>
<li>Circumventing a ban by using the extra account
<blockquote>
<p>Note: If you resolve the ban reasons, you should be able to get unbanned.</p>
</blockquote></li>
<li>&quot;getting around&quot; a restriction</li>
<li>Acting as two different people</li>
</ul>
<p>The following uses are <strong>legal</strong> and should not be reported:</p>
<ul>
<li>Extra accounts for bots (these should state that they are sockpuppets and link to the owner)</li>
<li>When running a testing server, please create multiple accounts to test the views for multiple people.</li>
<li>Feeling what it is like as a new user</li>
</ul>
<h2>How do I catch a sockpuppet?</h2>
<p>You can't be sure. Some people have similar writing styles, may live together, share the same internet network, or use the same computer. What looks like a sockpuppet may be two people with similar writing styles using the same computer.</p>
<p>Here are some signs (but not immediate excuses):</p>
<h3>Similar visit times</h3>
<p>Sockpuppets may go online around the same time as their owner. But this is not enough evidence for accusation where there are thousands of users. It could also be that two <em>friends</em>, not <em>sockpuppets</em>, are going online together.</p>
<h3>Following another user around</h3>
<p>If a user continues acting with one user, e.g. replying to one single user's topics with perfect solutions, they could be a sockpuppet.</p>
<h3>Many actions that are minutes apart</h3>
<p>Same as the one on top, except the timestamps on the actions are available publicly. Thus, it is easier for anyone to catch them (see ยง How do I report a possible sockpuppet?)</p>
<h2>How to report sockpuppets</h2>
<p>Send a private message to an administrator. Your private message should include:</p>
<ul>
<li>The accused sockpuppets</li>
<li>The suspected puppet masters</li>
<li>Any evidence you have </li>
</ul>
<p>Be sure to include all evidence!</p>
<h2>I've been accused, and I...</h2>
<h3>don't have a sockpuppet!</h3>
<p>Respond to the administrator, with evidence to refute the sockpuppet claim. They probably won't ban you if you convince them.</p>
<h3>actually have one!</h3>
<p>Admit your mistake, and don't do it again.</p>